                    <div class="acsb-statement"><strong class="acsb-title-main">Accessibility Statement</strong><strong
                            class="acsb-title">Compliance status</strong>
                        <p>We firmly believe that the internet should be available and accessible to anyone and are
                            committed to providing a website that is accessible to the broadest possible audience,
                            regardless of ability.</p>
                        <p>To fulfill this, we aim to adhere as strictly as possible to the World Wide Web Consortium’s
                            (W3C) Web Content Accessibility Guidelines 2.1 (WCAG 2.1) at the AA level. These guidelines
                            explain how to make web content accessible to people with a wide array of disabilities.
                            Complying with those guidelines helps us ensure that the website is accessible to blind
                            people, people with motor impairments, visual impairment, cognitive disabilities, and more.
                        </p>
                        <p>This website utilizes various technologies that are meant to make it as accessible as
                            possible at all times. We utilize an accessibility interface that allows persons with
                            specific disabilities to adjust the website’s UI (user interface) and design it to their
                            personal needs.</p>
                        <p>Additionally, the website utilizes an AI-based application that runs in the background and
                            optimizes its accessibility level constantly. This application remediates the website’s
                            HTML, adapts its functionality and behavior for screen-readers used by blind users, and for
                            keyboard functions used by individuals with motor impairments.</p>
                        <p>If you wish to contact the website’s owner please use the website's form</p>
                        <strong class="acsb-title">Screen-reader and keyboard navigation</strong>
                        <p>Our website implements the ARIA attributes (Accessible Rich Internet Applications) technique,
                            alongside various behavioral changes, to ensure blind users visiting with screen-readers can
                            read, comprehend, and enjoy the website’s functions. As soon as a user with a screen-reader
                            enters your site, they immediately receive a prompt to enter the Screen-Reader Profile so
                            they can browse and operate your site effectively. Here’s how our website covers some of the
                            most important screen-reader requirements:</p>
                        <ol>
                            <li><strong class="acsb-title">Screen-reader optimization:&nbsp;</strong>we run a process
                                that learns the website’s components from top to bottom, to ensure ongoing compliance
                                even when updating the website. In this process, we provide screen-readers with
                                meaningful data using the ARIA set of attributes. For example, we provide accurate form
                                labels; descriptions for actionable icons (social media icons, search icons, cart icons,
                                etc.); validation guidance for form inputs; element roles such as buttons, menus, modal
                                dialogues (popups), and others.&nbsp;<br><br>Additionally, the background process scans
                                all of the website’s images. It provides an accurate and meaningful
                                image-object-recognition-based description as an ALT (alternate text) tag for images
                                that are not described. It will also extract texts embedded within the image using an
                                OCR (optical character recognition) technology. To turn on screen-reader adjustments at
                                any time, users need only to press the Alt+1 keyboard combination. Screen-reader users
                                also get automatic announcements to turn the Screen-reader mode on as soon as they enter
                                the website.<br><br>These adjustments are compatible with popular screen readers such as
                                JAWS, NVDA, VoiceOver, and TalkBack.<br><br></li>
                            <li><strong class="acsb-title">Keyboard navigation optimization:&nbsp;</strong>The
                                background process also adjusts the website’s HTML and adds various behaviors using
                                JavaScript code to make the website operable by the keyboard. This includes the ability
                                to navigate the website using the Tab and Shift+Tab keys, operate dropdowns with the
                                arrow keys, close them with Esc, trigger buttons and links using the Enter key, navigate
                                between radio and checkbox elements using the arrow keys, and fill them in with the
                                Spacebar or Enter key.<br><br>Additionally, keyboard users will find content-skip menus
